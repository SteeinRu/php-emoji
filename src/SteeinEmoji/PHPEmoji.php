    /***
     *  getCharacter
     *
     * @param string $character = The Constant Name
     * @return string
     * @throws UnknownException
     */
    public static function getCharacter(string $character) : string
    {
        $constant = static::convertCharToConst($character);

        if (!defined("static::{$constant}")) {
            throw UnknownException::create($character);
        }
        return constant('static::'.$constant);
    }

    /***
     * Convert character name in a constant
     *
     * @param string $characterName
     * @return string
     */
    protected static function convertCharToConst(string $characterName) : string
    {
        $partialConstantName = static::convertToSnakeCase($characterName);
        $constantName = 'CHAR_'.strtoupper($partialConstantName);
        return $constantName;
    }

    protected static function convertToSnakeCase(string $value) : string
    {
        if (!ctype_lower($value)) {
            $value = preg_replace('/\s+/', '', $value);
            $value = strtolower(preg_replace('/(.)(?=[A-Z])/', '$1'.'_', $value));
        }
        return $value;
    }

    public static function __callStatic(string $methodName, array $parameters) : string
    {
        return static::getCharacter($methodName);
    }

    public function version()
    {
        return self::VERSION;
    }
